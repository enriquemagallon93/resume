// Reads resumes/manifest.json and emits src/resume/defaultResume.generated.ts
// re-exporting the version named by `defaultFile`. This keeps the bundled /
// SSG default (what visitors see with JavaScript disabled) automatically in
// sync with the manifest: promoting a default is just a manifest edit + build.
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');

const manifest = JSON.parse(readFileSync(resolve(root, 'resumes/manifest.json'), 'utf8'));
const defaultFile = manifest.defaultFile;

if (!defaultFile) {
  throw new Error('[generate:default] resumes/manifest.json is missing "defaultFile"');
}

// Fail loudly at build time if the manifest points at a file that doesn't exist.
readFileSync(resolve(root, 'resumes', defaultFile));

const output = `// AUTO-GENERATED by scripts/generate-default-resume.mjs — do not edit.
// Mirrors resumes/manifest.json "defaultFile" so the bundled / SSG default
// (rendered even with JavaScript disabled) always matches the manifest.
import data from '../../resumes/${defaultFile}';

export const DEFAULT_RESUME_PATH = '${defaultFile}';
export default data;
`;

writeFileSync(resolve(root, 'src/resume/defaultResume.generated.ts'), output);
console.log(`[generate:default] bundled default = ${defaultFile}`);
